""" 
                                        ПЕРШЕ ЗАВДАННЯ
                                        
    У вас є текстовий файл, який містить інформацію про місячні заробітні плати розробників у вашій компанії. Кожен рядок у файлі
містить прізвище розробника та його заробітну плату, які розділені комою без пробілів.
Наприклад:

Alex Korp,3000
Nikita Borisenko,2000
Sitarama Raju,1000

    Ваше завдання - розробити функцію total_salary(path), яка аналізує цей файл і повертає загальну та середню суму заробітної
плати всіх розробників.

                        Вимоги до завдання:

1. Функція total_salary(path) має приймати один аргумент - шлях до текстового файлу (path).
2. Файл містить дані про заробітні плати розробників, розділені комами. Кожен рядок вказує на одного розробника.
3. Функція повинна аналізувати файл, обчислювати загальну та середню суму заробітної плати.
4. Результатом роботи функції є кортеж із двох чисел: загальної суми зарплат і середньої заробітної плати.


                        Рекомендації для виконання:

1. Використовуйте менеджер контексту with для читання файлів.
2. Пам'ятайте про встановлення кодування при відкриті файлів
3. Для розділення даних у кожному рядку можна застосувати метод split(',').
4. Обрахуйте загальну суму заробітної плати, а потім розділіть її на кількість розробників, щоб отримати середню зарплату.
5. Опрацьовуйте можливі винятки при роботі з файлами, такі як відсутність файлу.

                        Критерії оцінювання:

1. Функція повинна точно обчислювати загальну та середню суми.
2. Повинна бути обробка випадків, коли файл відсутній або пошкоджений.
3. Код має бути чистим, добре структурованим і зрозумілим.

                        Приклад використання функції:

total, average = total_salary("path/to/salary_file.txt")
print(f"Загальна сума заробітної плати: {total}, Середня заробітна плата: {average}")

                        Очікуваний результат:

Загальна сума заробітної плати: 6000, Середня заробітна плата: 2000
"""

def total_salary(path): 
    """ Функція для обчислення загальної та середньої суми заробітної плати робітників з файлу."""
    try:
        with open(path, 'r', encoding='utf-8') as file:
            # Ініціалізуємо змінні для загальної та середньої зарплати
            total = 0
            # Ініціалізуємо лічильник для кількості робітників
            count = 0
            
            for line in file:
                # Розділяємо рядок на ім'я та зарплату
                # Перший елемент не використовується, оскільки ми не потребуємо імені 
                # робітника, тому використовуємо лише зарплату
                _ , salary = line.strip().split(',')
                # Передбачаемо наявність копійок у зарплатні (число з плаваючою комою),
                #  та сумуємо зарплатню до загальної суми
                total += float(salary)
                # Збільшуємо лічильник робітників  
                count += 1 
            
            if count == 0:
                return (0, 0)  # Якщо немає робітників, повертаємо (0, 0)
            
            average = total / count 
            # Повертаємо загальну та середню зарплату, округлені до двох знаків після коми
            return (round(total, 2), round(average, 2))  
    
    except FileNotFoundError:
        print(f"Файл {path} не знайдено.")
        return (0, 0)
    except Exception as e:
        print(f"Сталася помилка: {e}")
        return (0, 0)
# Застосуємо функцію total_salary для файлу "Text_for_task1.txt" та виведемо результат
if __name__ == "__main__":
    total, average = total_salary("Text_for_task1.txt")
    print(f"Загальна сума заробітної плати: {total}, Середня заробітна плата: {average}")
