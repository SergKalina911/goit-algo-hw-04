# –Ü–º–ø–æ—Ä—Ç—É—î–º–æ –Ω–µ–æ–±—Ö—ñ–¥–Ω—ñ –º–æ–¥—É–ª—ñ
import sys
from pathlib import Path
from colorama import Fore, Style, init
# –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–∞—Ü—ñ—è colorama –¥–ª—è –ø—ñ–¥—Ç—Ä–∏–º–∫–∏ –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ –≤–∏–≤–µ–¥–µ–Ω–Ω—è
init(autoreset=True)
"""–§—É–Ω–∫—Ü—ñ—è init() –º–æ–∂–µ –ø—Ä–∏–π–º–∞—Ç–∏ –Ω–µ–æ–±–æ–≤"—è–∑–∫–æ–≤–∏–π –∞—Ä–≥—É–º–µ–Ω—Ç autoreset=True. –ö–æ–ª–∏ –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ –∑–Ω–∞—á–µ–Ω–Ω—è True,
Colorama –∞–≤—Ç–æ–º–∞—Ç–∏—á–Ω–æ —Å–∫–∏–¥–∞—î –∫–æ–ª—ñ—Ä —ñ —Å—Ç–∏–ª—å —Ç–µ–∫—Å—Ç—É –¥–æ —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∏—Ö –ø—ñ—Å–ª—è –∫–æ–∂–Ω–æ–≥–æ –æ–ø–µ—Ä–∞—Ç–æ—Ä–∞ print(), 
–∑–∞–ø–æ–±—ñ–≥–∞—é—á–∏ –Ω–µ–Ω–∞–≤–º–∏—Å–Ω–æ–º—É —Ä–æ–∑—Ç—ñ–∫–∞–Ω–Ω—é –∫–æ–ª—å–æ—Ä—ñ–≤ —É –Ω–∞—Å—Ç—É–ø–Ω—ñ —Ä—è–¥–∫–∏ –≤–∏–≤–æ–¥—É."""

# –§—É–Ω–∫—Ü—ñ—è –¥–ª—è —Ä–µ–∫—É—Ä—Å–∏–≤–Ω–æ–≥–æ –≤–∏–≤–µ–¥–µ–Ω–Ω—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
def print_directory_structure(path: Path, indent: str = ""):
    """–†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –≤–∏–≤–æ–¥–∏—Ç—å —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∑ –∫–æ–ª—å–æ—Ä–æ–≤–∏–º —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è–º.
    –ê—Ä–≥—É–º–µ–Ω—Ç–∏:
        path (Path): –®–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó, —è–∫—É –ø–æ—Ç—Ä—ñ–±–Ω–æ –≤–∏–≤–µ—Å—Ç–∏.
        indent (str): –í—ñ–¥—Å—Ç—É–ø –¥–ª—è –≤—ñ–∑—É–∞–ª—å–Ω–æ–≥–æ —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è.
    –ü–æ–≤–µ—Ä—Ç–∞—î:
        None """
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞, —á–∏ —ñ—Å–Ω—É—î —à–ª—è—Ö —ñ —á–∏ —î –≤—ñ–Ω –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é
    if not path.exists():
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: {path} –Ω–µ —ñ—Å–Ω—É—î.")
        return
    if not path.is_dir():
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: {path} –Ω–µ —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é.")
        return

    for item in path.iterdir():
        # iterdir() –ø–æ–≤–µ—Ä—Ç–∞—î —ñ—Ç–µ—Ä–∞—Ç–æ—Ä, —è–∫–∏–π –ø–µ—Ä–µ–±–∏—Ä–∞—î –≤—Å—ñ –µ–ª–µ–º–µ–Ω—Ç–∏ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó path.
        # –¶–∏–∫–ª –ø–µ—Ä–µ–±–∏—Ä–∞—î –∫–æ–∂–µ–Ω –µ–ª–µ–º–µ–Ω—Ç —É –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó —Ç–∞ –≤–∏–≤–æ–¥–∏—Ç—å –π–æ–≥–æ –∑ –≤—ñ–¥–ø–æ–≤—ñ–¥–Ω–∏–º –≤—ñ–¥—Å—Ç—É–ø–æ–º.
        # –í—ñ–¥–æ–±—Ä–∞–∂–µ–Ω–Ω—è —ñ–º–µ–Ω—ñ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∞–±–æ —Ñ–∞–π–ª—É –∑ –∫–æ–ª—å–æ—Ä–æ–≤–∏–º —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è–º
        
        if item.is_dir():
            # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—î—é, –≤–∏–≤–æ–¥–∏–º–æ –π–æ–≥–æ –∑ —Å–∏–Ω—ñ–º –∫–æ–ª—å–æ—Ä–æ–º
            # –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è Fore.BLUE –¥–ª—è –∫–æ–ª—å–æ—Ä–æ–≤–æ–≥–æ –≤–∏–≤–µ–¥–µ–Ω–Ω—è —ñ–º–µ–Ω—ñ –¥–∏—Ä–µ–∫—Ç–æ—Ä
            print(f"{indent}{Fore.BLUE}{item.name}/")
            
            # –†–µ–∫—É—Ä—Å–∏–≤–Ω–∏–π –≤–∏–∫–ª–∏–∫ –¥–ª—è –≤–∏–≤–µ–¥–µ–Ω–Ω—è –≤–º—ñ—Å—Ç—É –ø—ñ–¥–¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∑ –¥–æ–¥–∞—Ç–∫–æ–≤–∏–º –≤—ñ–¥—Å—Ç—É–ø–æ–º
            # –î–æ–¥–∞—î–º–æ –¥–≤–∞ –ø—Ä–æ–±—ñ–ª–∏ –¥–æ –≤—ñ–¥—Å—Ç—É–ø—É –¥–ª—è –≤—ñ–∑—É–∞–ª—å–Ω–æ–≥–æ —Ñ–æ—Ä–º–∞—Ç—É–≤–∞–Ω–Ω—è
            # –¶–µ –¥–æ–∑–≤–æ–ª—è—î —Å—Ç–≤–æ—Ä–∏—Ç–∏ —ñ—î—Ä–∞—Ä—Ö—ñ—á–Ω—É —Å—Ç—Ä—É–∫—Ç—É—Ä—É –≤–∏–≤–æ–¥—É
            print_directory_structure(item, indent + "  ")
        else:
            # –Ø–∫—â–æ –µ–ª–µ–º–µ–Ω—Ç —î —Ñ–∞–π–ª–æ–º, –≤–∏–≤–æ–¥–∏–º–æ –π–æ–≥–æ –∑ –∑–µ–ª–µ–Ω–∏–º –∫–æ–ª—å–æ—Ä–æ–º
            print(f"{indent}{Fore.GREEN}{item.name}")

if __name__ == "__main__":
    
    # –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ –Ω–∞—è–≤–Ω–æ—Å—Ç—ñ –∞—Ä–≥—É–º–µ–Ω—Ç—ñ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ —Ä—è–¥–∫–∞
    if len(sys.argv) != 2:
        print(f"{Fore.RED}–ü–æ–º–∏–ª–∫–∞: –ë—É–¥—å –ª–∞—Å–∫–∞, –≤–∫–∞–∂—ñ—Ç—å —à–ª—è—Ö –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó.")
        # –í–∏–≤–µ—Å—Ç–∏ –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ –ø–æ–º–∏–ª–∫—É —Ç–∞ –∑–∞–≤–µ—Ä—à–∏—Ç–∏ –ø—Ä–æ–≥—Ä–∞–º—É
        sys.exit(1)
        """ –ó–∞ –∑–∞–º–æ–≤—á—É–≤–∞–Ω–Ω—è–º sys.exit() –ø–æ–≤–µ—Ä—Ç–∞—î –∫–æ–¥ –≤–∏—Ö–æ–¥—É 0, —â–æ –æ–∑–Ω–∞—á–∞—î —É—Å–ø—ñ—à–Ω–µ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è, –∞–ª–µ 
            –º–æ–∂–Ω–æ –≤–∫–∞–∑–∞—Ç–∏ —ñ–Ω—à–∏–π –∫–æ–¥ –ø–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è, —â–æ–± –ø–æ–≤—ñ–¥–æ–º–∏—Ç–∏ –ø—Ä–æ –ø–æ–º–∏–ª–∫—É –∞–±–æ —ñ–Ω—à–µ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è."""
    
    # –û—Ç—Ä–∏–º–∞–Ω–Ω—è —à–ª—è—Ö—É –¥–æ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó –∑ –∞—Ä–≥—É–º–µ–Ω—Ç—ñ–≤ –∫–æ–º–∞–Ω–¥–Ω–æ–≥–æ —Ä—è–¥–∫–∞. –í–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–Ω—è pathlib –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ —à–ª—è—Ö–∞–º–∏
    directory_path = Path(sys.argv[1])
    # –í–∏–∫–ª–∏–∫ —Ñ—É–Ω–∫—Ü—ñ—ó –≤–∏–≤–µ–¥–µ–Ω–Ω—è —Å—Ç—Ä—É–∫—Ç—É—Ä–∏ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó
    print_directory_structure(directory_path)
    # –í–∏–≤–µ–¥–µ–Ω–Ω—è –ø–æ–≤—ñ–¥–æ–º–ª–µ–Ω–Ω—è –ø—Ä–æ —É—Å–ø—ñ—à–Ω–µ –∑–∞–≤–µ—Ä—à–µ–Ω–Ω—è
    print(f"{Fore.YELLOW}–°—Ç—Ä—É–∫—Ç—É—Ä–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä—ñ—ó {directory_path} –≤–∏–≤–µ–¥–µ–Ω–∞ —É—Å–ø—ñ—à–Ω–æ.")
